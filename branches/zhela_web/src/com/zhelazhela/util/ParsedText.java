package com.zhelazhela.util;

public class ParsedText {
    private String remainText = null;

    private String resultText = null;

    /**
     * @return Returns the remainText.
     */
    public String getRemainText() {
        return remainText;
    }

    /**
     * @param remainText The remainText to set.
     */
    public void setRemainText(String remainText) {
        this.remainText = remainText;
    }

    /**
     * @return Returns the resultText.
     */
    public String getResultText() {
        return resultText;
    }

    /**
     * @param resultText The resultText to set.
     */
    public void setResultText(String resultText) {
        this.resultText = resultText;
    }

}
